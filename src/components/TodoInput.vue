<template>
    <div class="inputBox shadow">
        <input type="text" v-model="newTodoItem" v-on:keyup.enter="addTodo">
        <span class="addContainer" v-on:click="addTodo">
            <i class="material-icons addBtn">add</i>
        </span>
    </div>
</template>

<script>
export default {
    data() {
        return {
            newTodoItem: ""
        }
    },
    methods: {
        addTodo: function() {
            if(this.newTodoItem != ''){
                var obj = {completed: false, item:this.newTodoItem};
                console.log(this.newTodoItem);
                //저장하는 로직
                localStorage.setItem(this.newTodoItem, JSON.stringify(obj)); // 스트링으로 변경되도록 함
                this.clearInput();
            }
        },
        clearInput: function () {
            this.newTodoItem = '';
        }
    },
}
</script>

